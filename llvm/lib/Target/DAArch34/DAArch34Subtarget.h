#ifndef LLVM_LIB_TARGET_DAARCH34_DAARCH34SUBTARGET_H
#define LLVM_LIB_TARGET_DAARCH34_DAARCH34SUBTARGET_H

#include <string>

#include "llvm/ADT/StringRef.h"
#include "llvm/CodeGen/SelectionDAGTargetInfo.h"

#include "DAArch34FrameLowering.h"
#include "DAArch34ISelLowering.h"
#include "DAArch34InstrInfo.h"
#include "DAArch34RegisterInfo.h"

#define GET_SUBTARGETINFO_HEADER // for DAArch34GenSubtargetInfo
#include "DAArch34GenSubtargetInfo.inc"

namespace llvm {

class TargetMachine;
class Triple;

class DAArch34Subtarget : public DAArch34GenSubtargetInfo {
public:
  DAArch34Subtarget(const TargetMachine &TM, const Triple &TT,
                    const std::string &CPU, const std::string &FS);

  /// ParseSubtargetFeatures - Parses features string setting specified
  /// subtarget options. Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef TuneCPU, StringRef FS);

  const DAArch34TargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }

  const DAArch34FrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }

  const DAArch34RegisterInfo *getRegisterInfo() const override {
    return &RegInfo;
  }

  const DAArch34InstrInfo *getInstrInfo() const override { return &InstrInfo; }

  const SelectionDAGTargetInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }

private:
  DAArch34TargetLowering TLInfo;
  DAArch34FrameLowering FrameLowering;
  DAArch34RegisterInfo RegInfo;
  DAArch34InstrInfo InstrInfo;
  SelectionDAGTargetInfo TSInfo;
};

} // end namespace llvm

#endif // LLVM_LIB_TARGET_DAARCH34_DAARCH34SUBTARGET_H
